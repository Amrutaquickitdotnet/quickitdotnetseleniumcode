package javaprograms;


// This program illustrate Arithmatic operator
public class Example_008_logical_Operator 
{
	

public static void main(String[] args)// Static method 
{
	 boolean output1= true;
	 
	 boolean output2= false;
	 System.out.println("check if both the boolean variables are true :"+ (output1&&output2));
	 System.out.println("check if any one of  the boolean variables are true :"+ (output1||output2));
	 System.out.println("By using not operator:"+ !output1);
	 System.out.println("By using not operator:"+ !output2);
	
}
}
